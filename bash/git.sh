alias gco='g checkout'
alias gcb='gco -b'
alias gcl='g clone'
alias gcm='gfm && gf && gpl'
alias gd='g diff'
alias gf='g fetch'
alias gfm='gco main'
alias gi='g init'
alias gk='gitk'
alias gl='g log'
alias gm='g merge --no-ff --log'
alias gmm='gm main -m "Merged latest from main."'
alias gmv='g mv'
alias gp='g push'
alias gpo='gp origin'
alias gpu='gp -u origin'
alias gpl='g pull --no-edit'
alias grm='g rm'
alias gs='g stash'
alias gsl='gs list'
alias gsp='gs pop'
alias gst='g status'

